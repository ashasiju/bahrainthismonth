  function createCarousel(containerSelector, dotNavSelector, imageClass, dotClass) {
    const images = document.querySelectorAll(`${containerSelector} ${imageClass}`);
    const dotsContainer = document.querySelector(dotNavSelector);
    let currentIndex = 0;

    function showImage(index) {
        images.forEach((img, i) => {
            img.classList.toggle('active', i === index);
        });

        // Update dots
        const dots = document.querySelectorAll(`${dotNavSelector} ${dotClass}`);
        dots.forEach((dot, i) => {
            dot.classList.toggle('active', i === index);
        });
    }

    // Create dot navigation
    images.forEach((_, index) => {
        const dot = document.createElement('span');
        dot.classList.add(dotClass.slice(1)); // Remove '.' from class name for add
        dot.setAttribute('aria-label', `Go to slide ${index + 1}`);
        dot.addEventListener('click', () => {
            currentIndex = index;
            showImage(currentIndex);
        });
        dotsContainer.appendChild(dot);
    });

    // Auto change images every 3 seconds
    setInterval(() => {
        currentIndex = (currentIndex + 1) % images.length;
        showImage(currentIndex);
    }, 3000);

    // Show the first image and dots
    showImage(currentIndex);
}
